        <h2>Protocolos de la prueba de dislexia</h2>
        <h3>Protocolo de oído y lenguaje</h3>
        <p>En esta prueba el niño escuchara de a dos palabras y el tendrá que decir si las palabras que escucho son
            iguales o distintas, por ejemplo “bandera y pantera “en donde el deberá responder que las palabras son
            diferentes o “vaca y vaca” en donde deberá responder que son iguales.
            El puntaje que el niño obtendrá en esta prueba es el número de respuestas correctas.
        </p>
        <img src="images/dislexya/protocols/rfn004.png" alt="">

        <h3>Protocolo de escritura y habilidades motoras</h3>
        <p>En esta prueba el niño vera en la parte superior del dispositivo un par de flechas una verde y una roja, lo
            que el tendrá que hacer es recorrer con la mirada el conjunto de flechas que está en la parte inferior y
            deberá seleccionar las flechas que sean iguales a la flecha verde (Esta flecha es considerada como el
            estímulo blanco) pero la seleccionara solo cuando este después de una flecha como la roja (esta flecha es
            considerada el estímulo de aviso o señal)
            En esta prueba el niño obtendrá tres puntajes distintos, el primero es el número de flechas que identifico
            correctamente que será el número de respuestas correctas, el segundo el número de flechas que omitió que
            serán los errores de omisión y el tercero el número de flechas incorrectas que serán los errores de
            comisión.
        </p>
        <img src="images/dislexya/protocols/rfn005.png" alt="">

        <h3>Protocolo de visión, lectura y ortografía</h3>
        <p>En esta prueba el niño vera una serie de palabras, algunas de las palabras son reales y las otras son
            inventadas, lo que el niño tendrá que hacer es oprimir el marciano con cara feliz si la palabra existe de lo
            contrario deberá presionar el marciano con cara triste.
            En esta prueba se obtendrá dos puntajes el primero será el tiempo que el niño tardo en resolver la prueba y
            el segundo es la cantidad de errores en la clasificación.
        </p>
        <img src="./images/dislexya/protocols/rfn006.png" alt="">

        <h3>Protocolo de lenguaje</h3>
        <p>En esta prueba el niño escuchara una serie de palabras (algunas son palabras poco conocidas), las cuales
            escuchara de una en una lo que el deberá hacer es repetir la palabra oprimiendo el botón del micrófono.
            El puntaje que el niño obtendrá en esta prueba será el número de palabras que logro repetir de manera
            correcta.
        </p>
        <img src="images/dislexya/protocols/rfn007.png" alt="">

        <h3>Protocolo de memoria y cognición </h3>
        <p>En esta prueba el niño escuchara un serie de nombres de consonantes pronunciadas una por segundo, lo que el
            deberá hacer es presionar las consonantes en el mismo orden en que se mencionaron
            El puntaje será el máximo número correcto de consonantes repetidas (Puntuación máxima = 7)
        </p>
        <img src="images/dislexya/protocols/rfn008.png" alt="">

        <h3>Protocolo general</h3>
        <p>En esta prueba el niño vera una serie de palabras de las cuales algunas están mal escritas lo que el deberá
            hacer es seleccionar si la palabra está escrita o no correctamente en caso de que el seleccione que está
            escrita de manera incorrecta deberá escribir la forma en la que esta se escribe.
            Las respuestas correctas será el número de palabras que identifique correctamente, en caso de que la palabra
            sea incorrecta el punto se considerara si escribe de manera correcta la palabra.
        </p>
        <img src="images/dislexya/protocols/rfn009.png" alt="">

